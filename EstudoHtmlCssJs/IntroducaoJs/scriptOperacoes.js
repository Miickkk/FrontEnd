/*Declaração de variavel*/
let a = 10;
let b = 3;

/*Operadores Aritméticos*/
console.log(a + b); // 13
console.log(a - b); // 7
console.log(a * b); // 30
console.log(a / b); // 3.33
console.log(a % b); // 1

/*Operadores Relacionais*/
console.log(10 > 5);  // true
console.log(10 === "10"); // false (tipos diferentes)
console.log(10 == "10");  // true (conversão implícita)

/*Operadores Lógicos*/
let idade = 20;
console.log(idade > 18 && idade < 30); // true
console.log(idade > 18 || idade > 65); // false
console.log(idade > 18 || idade > 65); // false
console.log(idade < 35 || idade > 65); // true
console.log(!false); // true
